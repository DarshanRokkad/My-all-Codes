// Data types in c 
#include<stdio.h>
int main()
{
    int age = 18;
    float gpa = 8.8;
    double cgpa = 9.8;
    char grade = 'A' ;
    char name[]="Jenny khatri lamba";
    printf("Int = %d \n",age );
    printf("float  = %f \n",gpa );
    printf("double = %lf \n",cgpa );
    printf("character = %c\n",grade );
    printf("string (character array) = %s \n",name );

    return 0 ;
}